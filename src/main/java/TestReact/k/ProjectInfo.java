package TestReact.k;

public class ProjectInfo
{
    int id;
    long UID;
    String ProName;
    String Cate;
    String Date;
    String Info;
    String Path;

    public ProjectInfo(int id, long uid, String name, String cate, String date, String info, String path)
    {
        this.id = id;
        this.UID = uid;
        this.ProName = name;
        this.Cate = cate;
        this.Date = date;
        this.Info = info;
        this.Path = path;
    }
}
